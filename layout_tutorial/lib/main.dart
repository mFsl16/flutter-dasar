}

class FavoritWidget extends StatefulWidget {
  const FavoritWidget({Key? key}) : super(key: key);

  @override
  State<FavoritWidget> createState() => _FavoritWidgetState();
}

class _FavoritWidgetState extends State<FavoritWidget> {
  bool _isFavorite = false;
  int _favoriteCount = 41;

  void _toogleFavorite() {
    setState(() {
      if (_isFavorite) {
        _favoriteCount -= 1;
        _isFavorite = false;
      } else {
        _favoriteCount += 1;
        _isFavorite = true;
      }
    });
  }

  @override
  Widget build(BuildContext context) {
    return Row(
      mainAxisSize: MainAxisSize.min,
      children: [
        Container(
          padding: const EdgeInsets.all(0),
          child: IconButton(
            icon: _isFavorite
                ? const Icon(Icons.star)
                : const Icon(Icons.star_border),
            color: Colors.red[500],
            onPressed: _toogleFavorite,
          ),
        ),
        SizedBox(
          child: Text('$_favoriteCount'),
        )
      ],
    );
  }
}
